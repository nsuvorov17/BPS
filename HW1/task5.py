import math
a,b,c=map(float, input().split())
d=b**2-4*a*c
if a == 0 and b != 0:
    x=c/-b
    print('X:', x, sep='')
elif a == 0 and b == 0:
    x=c
    print('X:', x, sep='')
else:
    if d > 0:
        x1=(-b+math.sqrt(d))/(2*a)
        x2=(-b-math.sqrt(d))/(2*a)
        print('X1:'+str(x1), 'X2:'+str(x2))
    elif d == 0:
        x=(b*-1)/(2*a)
        print('X1:', x, sep='')
    else:
        print(' ')
